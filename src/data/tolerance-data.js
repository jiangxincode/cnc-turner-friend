/**
 * 根据公称直径查找细分段行
 */
function findDevRow(d) {
  for (const row of SHAFT_DEV_TABLE) {
    if (d > row[0] && d <= row[1]) return row;
  }
  if (d === 0) return SHAFT_DEV_TABLE[0];
  return null;
}

/**
 * 获取轴基本偏差 es（a~h）或 ei（j~zc），单位 μm
 * @param {string} band 公差带字母（小写）
 * @param {number} d 公称直径
 * @param {string} grade IT等级字符串，如 'IT7'
 * @returns {number|null}
 */
export function getShaftBaseDev(band, d, grade) {
  const row = findDevRow(d);
  if (!row) return null;

  const itNum = parseInt(grade.replace('IT', ''));

  if (band === 'h') return 0;
  if (band === 'js') return null; // 特殊处理

  if (band === 'j') {
    if (itNum <= 6) return row[2 + COL.j56];
    if (itNum === 7) return row[2 + COL.j7];
    if (itNum === 8) return row[2 + COL.j8];
    return 0; // IT9以上 j 偏差为0（实际上j不用于IT9+）
  }

  if (band === 'k') {
    if (itNum >= 4 && itNum <= 7) return row[2 + COL.k47];
    return row[2 + COL.ko]; // ≤IT3 或 >IT7
  }

  const colKey = band.toLowerCase();
  if (!(colKey in COL)) return null;
  const val = row[2 + COL[colKey]];
  return val === null || val === undefined ? null : val;
}

/**
 * 获取孔基本偏差 EI（A~H）或 ES（J~ZC），单位 μm
 * 孔的基本偏差与轴对称（符号相反），但有特殊规则
 */
export function getHoleBaseDev(band, d, grade) {
  const lower = band.toLowerCase();
  const shaftVal = getShaftBaseDev(lower, d, grade);
  if (shaftVal === null) return null;

  // A~H: EI = -es_shaft（正值）
  // J~ZC: ES = -ei_shaft（负值）
  // JS 特殊处理
  return -shaftVal;
}

/**
 * 查找公称直径对应的 IT 公差值（粗分段）
 */
export function getITValue(d, grade) {
  for (const row of TOLERANCE_DATA) {
    if (d > row.min && d <= row.max) return row[grade];
  }
  if (d === 0) return TOLERANCE_DATA[0][grade];
  return null;
}
